# calculates how much time is in a year, with different units.

days = 365
hours = 24 * days
minutes = 60 * hours
seconds = 60 * minutes

print ("one year has:")
print ("\t"+str(days)+"days")
print ("\t"+str(hours)+" hours")
print ("\t"+str(minutes)+" minutes")
print ("\t"+str(seconds)+" seconds")

